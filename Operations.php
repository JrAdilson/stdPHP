<?php
// Operações matemáticas
$idade = 22;
// qual sua idade daqui a 10 anos?
$idadeNew = $idade + 10;
// qual sua idade 10 anos atrás?
$idadeOld = $idade - 10;
// quantos anos você teria se tivesse o dobro da sua idade?
$idadeDouble = $idade * 2;
// quantos anos você teria se tivesse metade da sua idade?
$idadeHalf = $idade/2;
// sua idade ao cubo
$idadeCube = $idade ** 3;
// resto da sua idade, dividido por 3
$idadeRest = $idade%3;
echo $idadeNew;
echo "\n";
echo $idadeOld;
echo "\n";
echo $idadeDouble;
echo "\n";
echo $idadeHalf;
echo "\n";
echo $idadeCube;
